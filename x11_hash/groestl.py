def B64(n, x):
    if n == 7:
        return x.lo & 0xFF
    bits = (7 - n) * 8
    if bits >= 32:
        return op.rshift32b(x.hi, bits - 32) & 0xFF
    return op.rshift32b(x.lo, bits) & 0xFF


