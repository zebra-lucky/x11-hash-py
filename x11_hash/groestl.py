def groestl_update(ctx, msg, msg_len=None):
    V = [None] * 16
    buf = ctx['buffer']
    buf_len = len(buf)
    ptr = ctx['ptr']
    msg_len = len(msg) if msg_len is None else msg_len
    if msg_len < buf_len - ptr:
        op.buffer_insert(buf, ptr, msg, msg_len)
        ptr += msg_len
        ctx['ptr'] = ptr
        return

    op.buffer_insert(V, 0, ctx['state'], 16)
    while msg_len > 0:
        clen = buf_len - ptr
        if clen > msg_len:
            clen = msg_len
        op.buffer_insert(buf, ptr, msg, clen)
        ptr += clen
        msg = msg[clen:]
        msg_len -= clen
        if ptr == buf_len:
            buf64 = op.bytes_to_u64_list(buf, buf_len)
            compress(buf64, V)
            ctx['count'].add_one()
            ptr = 0

    ctx['state'] = V
    ctx['ptr'] = ptr


def groestl_close(ctx):
    buf = ctx['buffer']
    ptr = ctx['ptr']
    pad = [None] * 136
    pad[0] = 0x80
    if ptr < 120:
        padLen = 128 - ptr
        count = ctx['count'].plus(u64(0, 1))
    else:
        padLen = 256 - ptr
        count = ctx['count'].plus(u64(0, 2))

    set_len = padLen - 9
    pad[1:1+set_len] = [0] * set_len
    c_offset = padLen - 8
    pad[c_offset:c_offset+4] = list(map(lambda x: int(x), pack('>I', count.hi)))
    pad[c_offset+4:c_offset+8] = list(map(lambda x: int(x), pack('>I', count.lo)))
    groestl_update(ctx, pad, padLen)
    final(ctx['state'])
    state = ctx['state']
    out = [None] * 16
    for u in range(8):
        v = u + 8
        out[2 * u] = state[v].hi
        out[2 * u + 1] = state[v].lo
    return out


def pack_state(state):
    res = b''
    for i in state:
        res += pack('>I', i)
    return res


def groestl(msg):
    ctx = {}
    state = [None] * 16
    for i in range(15):
        state[i] = u64(0, 0)
    state[15] = u64(0, 512)
    ctx['state'] = state
    ctx['ptr'] = 0
    ctx['count'] = u64(0, 0)
    ctx['buffer'] = bytearray(128)
    groestl_update(ctx, msg)
    res = groestl_close(ctx)
    res = pack_state(res)
    pprint(res.hex())
